"""
Layer 2: Autonomous Planning Agent
====================================
Uses LangGraph to orchestrate a Tree-of-Thought reasoning loop.
Generates 3 fix strategies, scores each, and selects the winning plan.

The plan drives every subsequent layer — it is the 'specification'
the code generation layer works from.

Technologies: LangGraph, GPT-4o API, Tree-of-Thought prompting, Pydantic
"""

import os
import json
from typing import Annotated, TypedDict, Optional
from dataclasses import dataclass

from pydantic import BaseModel, Field
from rich.console import Console

console = Console()


# ── Data Models ──────────────────────────────────────────────────────────────

class FixStrategy(BaseModel):
    """A single candidate fix strategy generated by Tree-of-Thought."""
    strategy_id: str = Field(description="A, B, or C")
    hypothesis: str = Field(description="Root cause hypothesis")
    approach: str = Field(description="How to fix it")
    affected_files: list[str] = Field(description="Files that need changes")
    affected_functions: list[str] = Field(description="Functions that need changes")
    risk_level: str = Field(description="low | medium | high")
    confidence_score: float = Field(description="0.0 to 1.0 confidence score")
    reasoning: str = Field(description="Why this strategy scores as it does")


class RepairPlan(BaseModel):
    """The complete structured plan output by Layer 2."""
    issue_summary: str = Field(description="One-line summary of the issue")
    root_cause: str = Field(description="Identified root cause")
    selected_strategy: FixStrategy = Field(description="The winning strategy")
    all_strategies: list[FixStrategy] = Field(description="All 3 generated strategies")
    target_files: list[str] = Field(description="Files to be modified")
    target_functions: list[str] = Field(description="Functions to be modified")
    expected_test_outcome: str = Field(description="What passing tests should look like after fix")
    risk_flags: list[str] = Field(description="Potential side effects or risks")


# ── LangGraph State ──────────────────────────────────────────────────────────

class PlanningState(TypedDict):
    """State passed between nodes in the LangGraph planning pipeline."""
    issue_text: str
    retrieved_chunks: list[dict]       # From Layer 1
    call_graph_context: str            # Relevant call graph subgraph
    strategies: list[FixStrategy]
    repair_plan: Optional[RepairPlan]
    error: Optional[str]
    memory_context: Optional[str]      # From Layer 7 (past similar issues)


# ── Prompts ──────────────────────────────────────────────────────────────────

